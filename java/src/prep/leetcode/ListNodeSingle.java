package prep.leetcode;

public class ListNodeSingle 
{
      int val;
      ListNodeSingle next;
      
      ListNodeSingle() {}
      
      ListNodeSingle(int val) 
      { 
    	  this.val = val; 
      }
      
      ListNodeSingle(int val, ListNodeSingle next) 
      { 
    	  this.val = val; 
    	  this.next = next; 
	  }
 }
